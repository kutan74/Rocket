import UIKit

extension UIColor {
    @nonobjc class var greyishBrown: UIColor {
        return UIColor(white: 77.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var blackBg: UIColor {
        return UIColor(white: 39.0 / 255.0, alpha: 1.0)
    }
}
